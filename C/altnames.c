#include <stdio.h>
#include <inttypes.h>
int main() {
    int32_t a = 123456789; // 32位有符号整数，取值范围为-2147483648到2147483647
    long64_t b = 1234567890123456789; // 64位有符号整数，取值范围为-9223372036854775808到9223372036854775807
    printf("a = %d\n", a); // 输出a的值，结果为123456789
    getchar(); // 不希望执行的窗口立即结束
    return 0;
}